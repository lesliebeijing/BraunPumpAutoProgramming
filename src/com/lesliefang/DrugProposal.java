package com.lesliefang;

public class DrugProposal {
    private String drugId;
    private String drugName;
    private String drugShort;
    private String rate;
    private String vtbi;
    private String amount;
    private String volume;
    private String orderNumber;

    public String getDrugId() {
        return drugId;
    }

    public void setDrugId(String drugId) {
        this.drugId = drugId;
    }

    public String getDrugName() {
        return drugName;
    }

    public void setDrugName(String drugName) {
        this.drugName = drugName;
    }

    public String getDrugShort() {
        return drugShort;
    }

    public void setDrugShort(String drugShort) {
        this.drugShort = drugShort;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getVtbi() {
        return vtbi;
    }

    public void setVtbi(String vtbi) {
        this.vtbi = vtbi;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getVolume() {
        return volume;
    }

    public void setVolume(String volume) {
        this.volume = volume;
    }

    public String getOrderNumber() {
        return orderNumber;
    }

    public void setOrderNumber(String orderNumber) {
        this.orderNumber = orderNumber;
    }

}
